// ── REVIEWS CAROUSEL ──
(function(){
  const track = document.getElementById('revTrack');
  const dotsEl = document.getElementById('revDots');
  if(!track) return;

  let current = 0;
  let perView = 3;
  let isDragging = false, startX = 0, dragOffset = 0;

  function getPerView(){
    if(window.innerWidth <= 768) return 1;
    if(window.innerWidth <= 1000) return 2;
    return 3;
  }

  function setup(){
    perView = getPerView();
    const cards = track.querySelectorAll('.rev-card');
    const total = cards.length;
    const pages = Math.ceil(total / perView);
    // clamp current
    if(current > pages - 1) current = pages - 1;
    // dots
    dotsEl.innerHTML = '';
    for(let i=0;i<pages;i++){
      const d = document.createElement('div');
      d.className = 'rev-dot' + (i===current?' active':'');
      d.onclick = ()=>{ current=i; render(); };
      dotsEl.appendChild(d);
    }
    render();
  }

  function render(){
    const cards = track.querySelectorAll('.rev-card');
    const total = cards.length;
    const pages = Math.ceil(total / perView);
    const gap = 16;
    const cardW = (track.parentElement.offsetWidth - gap*(perView-1)) / perView;
    const offset = current * (cardW + gap) * perView;
    track.style.transform = `translateX(-${offset}px)`;
    // update dots
    dotsEl.querySelectorAll('.rev-dot').forEach((d,i)=>d.classList.toggle('active',i===current));
    // arrows
    document.getElementById('revPrev').style.opacity = current===0?'0.35':'1';
    document.getElementById('revNext').style.opacity = current>=pages-1?'0.35':'1';
  }

  window.revMove = function(dir){
    const cards = track.querySelectorAll('.rev-card');
    const pages = Math.ceil(cards.length / perView);
    current = Math.max(0, Math.min(pages-1, current+dir));
    render();
  };

  // drag / swipe
  track.addEventListener('mousedown', e=>{ isDragging=true; startX=e.clientX; track.style.transition='none'; });
  document.addEventListener('mousemove', e=>{ if(!isDragging) return; dragOffset=e.clientX-startX; });
  document.addEventListener('mouseup', e=>{
    if(!isDragging) return; isDragging=false;
    track.style.transition='';
    if(Math.abs(dragOffset)>50) revMove(dragOffset<0?1:-1);
    else render();
    dragOffset=0;
  });
  track.addEventListener('touchstart', e=>{ startX=e.touches[0].clientX; track.style.transition='none'; },{passive:true});
  track.addEventListener('touchend', e=>{
    track.style.transition='';
    const diff = e.changedTouches[0].clientX - startX;
    if(Math.abs(diff)>50) revMove(diff<0?1:-1);
    else render();
  });

  window.addEventListener('resize', setup);
  setup();
})();

