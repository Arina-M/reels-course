const SELECTORS = {
  languageButton: "[data-language]",
  mobileNav: "#mobileNav",
  menuToggle: "#menu-toggle",
  reveal: ".reveal",
  reviewCard: ".review-card",
  reviewNavButton: "[data-carousel-direction]",
  reviewDot: ".reviews-dot",
  reviewsNext: "#reviewsNext",
  reviewsPrev: "#reviewsPrev",
  reviewsTrack: "#reviewsTrack",
  reviewsDots: "#reviewsDots",
  scrollButton: "[data-scroll-target]",
  signupForm: "#signupForm",
  successMessage: "#successMessage",
  translatable: "[data-i18n]",
};

const CSS_CLASSES = {
  active: "active",
  open: "open",
  visible: "visible",
};

const REVIEW_CARD_GAP = 16;
const SWIPE_THRESHOLD = 50;

let currentLanguage = "uk";

function getElement(selector) {
  return document.querySelector(selector);
}

function setLanguage(language) {
  if (!i18n[language]) return;

  currentLanguage = language;
  document.documentElement.lang = language;

  document.querySelectorAll(SELECTORS.languageButton).forEach((button) => {
    button.classList.toggle(
      "on",
      button.dataset.language === currentLanguage,
    );
  });

  document.querySelectorAll(SELECTORS.translatable).forEach((element) => {
    const translationKey = element.dataset.i18n;
    const translation = i18n[currentLanguage][translationKey];

    if (translation) {
      element.innerHTML = translation;
    }
  });
}

function initLanguageSwitcher() {
  document.querySelectorAll(SELECTORS.languageButton).forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(button.dataset.language);
    });
  });
}

function initMobileNavigation() {
  const menuToggle = getElement(SELECTORS.menuToggle);
  const mobileNav = getElement(SELECTORS.mobileNav);

  if (!menuToggle || !mobileNav) return;

  const closeMobileNav = () => {
    menuToggle.classList.remove(CSS_CLASSES.open);
    mobileNav.classList.remove(CSS_CLASSES.open);
    document.body.style.overflow = "";
  };

  const toggleMobileNav = () => {
    const isOpen = mobileNav.classList.toggle(CSS_CLASSES.open);
    menuToggle.classList.toggle(CSS_CLASSES.open, isOpen);
    document.body.style.overflow = isOpen ? "hidden" : "";
  };

  menuToggle.addEventListener("click", toggleMobileNav);
  mobileNav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMobileNav);
  });
}

function initSmoothScrollButtons() {
  document.querySelectorAll(SELECTORS.scrollButton).forEach((button) => {
    button.addEventListener("click", () => {
      const target = document.getElementById(button.dataset.scrollTarget);
      target?.scrollIntoView({ behavior: "smooth" });
    });
  });
}

function initSignupForm() {
  const form = getElement(SELECTORS.signupForm);
  const successMessage = getElement(SELECTORS.successMessage);

  if (!form || !successMessage) return;

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    form.hidden = true;
    successMessage.hidden = false;
  });
}

function initReviewsCarousel() {
  const track = getElement(SELECTORS.reviewsTrack);
  const dotsContainer = getElement(SELECTORS.reviewsDots);
  const previousButton = getElement(SELECTORS.reviewsPrev);
  const nextButton = getElement(SELECTORS.reviewsNext);

  if (!track || !dotsContainer || !previousButton || !nextButton) return;

  let currentPage = 0;
  let cardsPerView = getCardsPerView();
  let dragStartX = 0;
  let dragDeltaX = 0;
  let isDragging = false;

  function getCardsPerView() {
    if (window.innerWidth <= 768) return 1;
    if (window.innerWidth <= 1000) return 2;
    return 3;
  }

  function getCards() {
    return [...track.querySelectorAll(SELECTORS.reviewCard)];
  }

  function getPageCount() {
    return Math.ceil(getCards().length / cardsPerView);
  }

  function clampPage(page) {
    return Math.max(0, Math.min(getPageCount() - 1, page));
  }

  function moveToPage(page) {
    currentPage = clampPage(page);
    render();
  }

  function renderDots() {
    dotsContainer.innerHTML = "";

    for (let page = 0; page < getPageCount(); page += 1) {
      const dot = document.createElement("button");
      dot.className = "reviews-dot";
      dot.type = "button";
      dot.setAttribute("aria-label", `Відгуки, сторінка ${page + 1}`);
      dot.addEventListener("click", () => moveToPage(page));
      dotsContainer.appendChild(dot);
    }
  }

  function render() {
    const carouselWidth = track.parentElement.offsetWidth;
    const cardWidth =
      (carouselWidth - REVIEW_CARD_GAP * (cardsPerView - 1)) / cardsPerView;
    const offset = currentPage * (cardWidth + REVIEW_CARD_GAP) * cardsPerView;

    track.style.transform = `translateX(-${offset}px)`;

    dotsContainer
      .querySelectorAll(SELECTORS.reviewDot)
      .forEach((dot, index) => {
        dot.classList.toggle(CSS_CLASSES.active, index === currentPage);
      });

    previousButton.style.opacity = currentPage === 0 ? "0.35" : "1";
    nextButton.style.opacity =
      currentPage >= getPageCount() - 1 ? "0.35" : "1";
  }

  function setup() {
    cardsPerView = getCardsPerView();
    currentPage = clampPage(currentPage);
    renderDots();
    render();
  }

  function moveBy(direction) {
    moveToPage(currentPage + direction);
  }

  function handleDragEnd(deltaX) {
    track.style.transition = "";

    if (Math.abs(deltaX) > SWIPE_THRESHOLD) {
      moveBy(deltaX < 0 ? 1 : -1);
      return;
    }

    render();
  }

  document.querySelectorAll(SELECTORS.reviewNavButton).forEach((button) => {
    button.addEventListener("click", () => {
      moveBy(Number(button.dataset.carouselDirection));
    });
  });

  track.addEventListener("mousedown", (event) => {
    isDragging = true;
    dragStartX = event.clientX;
    track.style.transition = "none";
  });

  document.addEventListener("mousemove", (event) => {
    if (!isDragging) return;
    dragDeltaX = event.clientX - dragStartX;
  });

  document.addEventListener("mouseup", () => {
    if (!isDragging) return;
    isDragging = false;
    handleDragEnd(dragDeltaX);
    dragDeltaX = 0;
  });

  track.addEventListener(
    "touchstart",
    (event) => {
      dragStartX = event.touches[0].clientX;
      track.style.transition = "none";
    },
    { passive: true },
  );

  track.addEventListener("touchend", (event) => {
    const deltaX = event.changedTouches[0].clientX - dragStartX;
    handleDragEnd(deltaX);
  });

  window.addEventListener("resize", setup);
  setup();
}

function initFaqAccordion() {
  document.querySelectorAll(".faq-list details").forEach((details) => {
    const summary = details.querySelector("summary");
    const answer = details.querySelector(".faq-answer");

    if (!summary || !answer) return;

    summary.addEventListener("click", (e) => {
      e.preventDefault();

      if (details.open) {
        answer.style.height = answer.offsetHeight + "px";
        answer.offsetHeight; // force reflow
        answer.style.height = "0";
        answer.style.paddingBottom = "0";

        answer.addEventListener(
          "transitionend",
          () => {
            details.removeAttribute("open");
            answer.style.height = "";
            answer.style.paddingBottom = "";
          },
          { once: true },
        );
      } else {
        details.setAttribute("open", "");
        const naturalHeight = answer.scrollHeight;
        answer.style.height = "0";
        answer.style.paddingBottom = "0";
        answer.offsetHeight; // force reflow
        answer.style.height = naturalHeight + "px";
        answer.style.paddingBottom = "20px";

        answer.addEventListener(
          "transitionend",
          () => {
            answer.style.height = "";
          },
          { once: true },
        );
      }
    });
  });
}

function initRevealAnimations() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add(CSS_CLASSES.visible);
        }
      });
    },
    { threshold: 0.08 },
  );

  document.querySelectorAll(SELECTORS.reveal).forEach((element) => {
    observer.observe(element);
  });
}

function initPage() {
  initLanguageSwitcher();
  initMobileNavigation();
  initSmoothScrollButtons();
  initSignupForm();
  initReviewsCarousel();
  initRevealAnimations();
  initFaqAccordion();
}

document.addEventListener("DOMContentLoaded", initPage);
